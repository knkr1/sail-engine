void MeshLibrary::init()
{
    meshes.emplace("cube", Mesh(cubeVertices));
}

Mesh* MeshLibrary::get(const std::string& name)
{
    auto it = meshes.find(name);
    if (it != meshes.end()) {
        return &it->second;
    }
    return nullptr;
}